package org.vgu.ocl2psql.ocl.exception;


public class OclEvaluationException extends OclException {

  private static final long serialVersionUID = 1L;

  public OclEvaluationException() {
    super();
  }

  public OclEvaluationException(String message, Throwable cause) {
    super(message, cause);
  }

  public OclEvaluationException(String message) {
    super(message);
  }

  public OclEvaluationException(Throwable cause) {
    super(cause);
  }

}
